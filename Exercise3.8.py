"""
This is a program that calculates and displays the value of a light-year.
"""

#declaration of variable and its value
light_travels = 3 * (10 ** 8) #light travels per second

#calculates for the light year
light_year = light_travels * 60 * 60* 24 * 365

#prints the light year
print("Light year:", light_year)
