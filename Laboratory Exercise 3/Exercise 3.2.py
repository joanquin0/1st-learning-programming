"""
This program calculates the surface area of the cube. Asking for the
lenght of an edge of the cube.
"""
#Asks the user to input the length of the edge of the cube
cube_Edge = int(input("Enter the length of the edge of a cube: "))

#Calculates the area of the cube
cube_Area = (cube_Edge ** 2) * 6

#Prints the area of the cube
print("The area of the cube is", cube_Area)

